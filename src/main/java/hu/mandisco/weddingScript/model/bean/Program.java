package hu.mandisco.weddingScript.model.bean;

public class Program {
	private int progId;
	private String name;

	public int getProgId() {
		return progId;
	}

	public void setProgId(int progId) {
		this.progId = progId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
