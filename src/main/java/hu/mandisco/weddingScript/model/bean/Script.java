package hu.mandisco.weddingScript.model.bean;

public class Script {
	private int scriptId;
	private String name;

	public int getScriptId() {
		return scriptId;
	}

	public void setScriptId(int scriptId) {
		this.scriptId = scriptId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
